package com.basics;


//This are built-in methods for MATHs.
public class mathClassMethods {
    public static void main(String[] args) {
        System.out.println(Math.abs(-26)); //will give you the absolute value of the number.
        System.out.println(Math.ceil(7.4)); //will give you a ROUNDUP the number.
        System.out.println(Math.floor(7.4)); //will give you a ROUND-DOWN the number.
        System.out.println(Math.max(8.6,5.2)); //will give you the maximum of listed numbers.
        System.out.println(Math.min(8.6, 5.2));//will give you the minimum of listed numbers.
        System.out.println(Math.pow(5,3)); //is also equivalent to 5^3
        System.out.println(Math.sqrt(9)); //will give you the square root of the number.
    }
}
